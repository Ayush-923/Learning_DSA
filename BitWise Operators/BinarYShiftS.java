public class BinarYShiftS {
   
    public static void main(String[] args) {
        //BINARRY LEFT SHIFT OPERATOR
        //FOR A << B(2)
        //IT WILL SHIFT THE BY 2 VALUES TOWARD LEFT
        // A << B =  A * 2^B 
        System.out.println(5<<2);
        


        //BINARRY RIGHT SHIFT
        //FOR A>>B = A / 2^B
        // 6 >> 1 = 6 / 2^1 = 6/2 = 3
        System.out.println(6 >> 1);

    }
}
